    public static void imprimirMensagemComMoldura(String mensagem) {
        int larguraMoldura = mensagem.length() + 4;
        String linhaMoldura = "+" + "-".repeat(larguraMoldura - 2) + "+";
        String mensagemComEspacos = "| " + String.format("%-" + (larguraMoldura - 4) + "s", mensagem) + " |";

        System.out.println(linhaMoldura);
        System.out.println(mensagemComEspacos);
        System.out.println(linhaMoldura);
    }

